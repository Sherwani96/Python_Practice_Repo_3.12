                # beside it, it must be readable by others or your co-workers
# predictability = e.g using new features, or some really obscure tools or functions that are not very common
                    # just to look well frankly smart
# DRY = do not repeat yourself means here do not copy paste the same code again and again try using counter or use looping or whatever
